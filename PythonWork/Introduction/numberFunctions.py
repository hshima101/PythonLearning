import math

# Constants
print("Math Constants:")
print("Pi:", math.pi)                # Value of Pi
print("E:", math.e)                  # Value of Euler's number (e)

# Basic Math Functions
x = 4.5
y = 2.0

print("\nBasic Math Functions:")
print("Addition:", x + y)            # Addition
print("Subtraction:", x - y)         # Subtraction
print("Multiplication:", x * y)      # Multiplication
print("Division:", x / y)            # Division

# More Advanced Math Functions
z = -3.0

print("\nMore Advanced Math Functions:")
print("Absolute Value:", abs(z))      # Absolute value
print("Square Root:", math.sqrt(x))   # Square root
print("Exponentiation:", math.exp(x)) # Exponential function
print("Logarithm (base 10):", math.log10(x))  # Base 10 logarithm
print("Trigonometric Functions:")
print("Sine:", math.sin(math.pi/2))  # Sine function (takes radians)
print("Cosine:", math.cos(0))        # Cosine function (takes radians)

# Round and Floor/Ceiling Functions
num = 4.75

print("\nRounding Functions:")
print("Round:", round(num))                   # Round to the nearest integer
print("Floor:", math.floor(num))              # Round down to the nearest integer
print("Ceiling:", math.ceil(num))             # Round up to the nearest integer

# Mathematical Constants
print("\nSome Other Constants:")
print("Infinity:", math.inf)         # Positive infinity
print("Negative Infinity:", -math.inf) # Negative infinity
print("NaN (Not-a-Number):", math.nan) # Represents undefined or unrepresentable value
